def _render_policy_ts(spec: BehaviorSpec) -> str:
    limit = spec.constraint(ConstraintKind.SPEND_LIMIT)
    threshold = float(limit.params.get("threshold", 50.0)) if limit else 50.0
    lines = [
        "// Executable behavior policy. Generated by AgentProof from the behavior spec.",
        "",
        f"export const AUTO_REFUND_LIMIT_USD = {threshold};",
        "",
        "export interface RefundDecision {",
        "  allowed: boolean;",
        "  requiresApproval: boolean;",
        "  reason: string;",
        "}",
        "",
        "export function checkRefund(amount: number, approvedByHuman = false): RefundDecision {",
        "  if (amount <= 0) return { allowed: false, requiresApproval: false, reason: 'non-positive amount' };",
        "  if (amount <= AUTO_REFUND_LIMIT_USD)",
        "    return { allowed: true, requiresApproval: false, reason: 'within auto-refund limit' };",
        "  if (approvedByHuman) return { allowed: true, requiresApproval: true, reason: 'approved by human' };",
        "  return { allowed: false, requiresApproval: true, reason: 'requires human approval' };",
        "}",
        "",
    ]
    if spec.constraint(ConstraintKind.PII_EGRESS):
        lines += [
            "export const PII_FIELDS = ['email', 'address', 'phone', 'card', 'ssn'];",
            "",
            "export function redactPii(payload: Record<string, unknown>): Record<string, unknown> {",
            "  const out: Record<string, unknown> = {};",
            "  for (const [k, v] of Object.entries(payload)) out[k] = PII_FIELDS.includes(k) ? '[REDACTED]' : v;",
            "  return out;",
            "}",
            "",
        ]
    if spec.constraint(ConstraintKind.PROMPT_INJECTION):
        lines += [
            "export const INJECTION_MARKERS = [",
            "  'ignore all previous', 'ignore your', 'system override', 'you are now',",
            "  'no approval needed', '[admin]', 'policy disabled', 'as a supervisor', 'i authorize',",
            "];",
            "",
            "export function quarantineUntrusted(text: string): { text: string; flagged: boolean } {",
            "  const lowered = text.toLowerCase();",
            "  const flagged = INJECTION_MARKERS.some((m) => lowered.includes(m));",
            "  return { text, flagged };",
            "}",
            "",
        ]
    return "\n".join(lines)


def _render_policy_test_ts(spec: BehaviorSpec, scenarios: list[Scenario]) -> str:
    limit = spec.constraint(ConstraintKind.SPEND_LIMIT)
    threshold = float(limit.params.get("threshold", 50.0)) if limit else 50.0
    extra_imports = []
    if spec.constraint(ConstraintKind.PII_EGRESS):
        extra_imports.append("redactPii")
    if spec.constraint(ConstraintKind.PROMPT_INJECTION):
        extra_imports.append("quarantineUntrusted")
    imports = "checkRefund" + ("".join(f", {n}" for n in extra_imports))
    lines = [
        "// Behavior tests generated by AgentProof. Runs on plain Node (node --test).",
        "import { test } from 'node:test';",
        "import assert from 'node:assert';",
        f"import {{ {imports} }} from '../agent/policy.ts';",
        "",
        "test('auto refund within limit', () => {",
        f"  const d = checkRefund({threshold} - 0.01);",
        "  assert.ok(d.allowed && !d.requiresApproval);",
        "});",
        "",
        "test('refund above limit requires approval', () => {",
        f"  const d = checkRefund({threshold} + 0.01);",
        "  assert.ok(!d.allowed && d.requiresApproval);",
        "});",
        "",
        "test('approved over-limit refund allowed', () => {",
        f"  assert.ok(checkRefund({threshold} * 2, true).allowed);",
        "});",
        "",
        "test('non-positive amounts rejected', () => {",
        "  assert.ok(!checkRefund(0).allowed);",
        "  assert.ok(!checkRefund(-5).allowed);",
        "});",
        "",
    ]
    if spec.constraint(ConstraintKind.PII_EGRESS):
        lines += [
            "test('PII redacted before egress', () => {",
            "  const r = redactPii({ email: 'a@b.com', order: '1', card: '4242' });",
            "  assert.strictEqual(r.email, '[REDACTED]');",
            "  assert.strictEqual(r.card, '[REDACTED]');",
            "  assert.strictEqual(r.order, '1');",
            "});",
            "",
        ]
    if spec.constraint(ConstraintKind.PROMPT_INJECTION):
        payloads = [
            s.user_message
            for s in scenarios
            if s.category == ScenarioCategory.PROMPT_INJECTION
        ][:3]
        lines.append("test('injection payloads flagged', () => {")
        for p in payloads:
            esc = p.replace("\\", "\\\\").replace("'", "\\'")
            lines.append(f"  assert.ok(quarantineUntrusted('{esc}').flagged);")
        lines += ["  assert.ok(!quarantineUntrusted('where is my order?').flagged);", "});", ""]
    return "\n".join(lines)


def _render_agent_ts(spec: BehaviorSpec, graph: AgentGraph) -> str:
    return (
        "// Framework-neutral agent entrypoint. Generated by AgentProof.\n"
        "// Wire your LLM client of choice here; the policy gate is enforced below.\n"
        "import { checkRefund } from './policy.ts';\n\n"
        "export function handleRefundRequest(amount: number, approvedByHuman = false): string {\n"
        "  const decision = checkRefund(amount, approvedByHuman);\n"
        "  if (!decision.allowed) return `BLOCKED by policy: ${decision.reason}`;\n"
        "  // ... call your refund tool here ...\n"
        "  return `refund of $${amount} processed`;\n"
        "}\n"
    )


def export_typescript(
    spec: BehaviorSpec, graph: AgentGraph, scenarios: list[Scenario], out_dir: str | Path
) -> list[Path]:
    out = Path(out_dir)
    files = {
        "agent/policy.ts": _render_policy_ts(spec),
        "agent/agent.ts": _render_agent_ts(spec, graph),
        "tests/policy.test.ts": _render_policy_test_ts(spec, scenarios),
        "package.json": (
            "{\n"
            f'  "name": "{_npm_slug(spec.name)}",\n'
            '  "version": "0.1.0",\n'
            '  "type": "module",\n'
            '  "scripts": { "test": "node --test --experimental-strip-types \\"tests/*.test.ts\\"" }\n'
            "}\n"
        ),
        "README.md": _readme(spec, "TypeScript", "npm test"),
        ".github/workflows/ci.yml": _ts_ci(),
    }
    return _write(out, files)


# ---------------------------------------------------------------------------
# shared helpers
# ---------------------------------------------------------------------------

def _npm_slug(name: str) -> str:
    return "".join(c if c.isalnum() else "-" for c in name.lower()).strip("-") or "agent"


def _readme(spec: BehaviorSpec, framework: str, run_cmd: str) -> str:
    return (
        f"# {spec.name} ({framework})\n\n"
        "Generated by [AgentProof](https://github.com/evanl666/agentproof) — "
        "structure verified by simulation before export.\n\n"
        f"```bash\n{run_cmd}\n```\n"
    )


def _py_ci() -> str:
    return (
        "name: agent-ci\non:\n  push:\n  pull_request:\n"
        "jobs:\n  behavior-tests:\n    runs-on: ubuntu-latest\n    steps:\n"
        "      - uses: actions/checkout@v4\n"
        "      - uses: actions/setup-python@v5\n        with:\n          python-version: '3.11'\n"
        "      - run: pip install pytest\n"
        "      - run: pytest tests/ -v\n"
    )


def _ts_ci() -> str:
    return (
        "name: agent-ci\non:\n  push:\n  pull_request:\n"
        "jobs:\n  behavior-tests:\n    runs-on: ubuntu-latest\n    steps:\n"
        "      - uses: actions/checkout@v4\n"
        "      - uses: actions/setup-node@v4\n        with:\n          node-version: '22'\n"
        "      - run: npm test\n"
    )


def _py_dockerfile(module: str) -> str:
    return (
        "FROM python:3.11-slim\nWORKDIR /app\n"
        "COPY requirements.txt .\nRUN pip install --no-cache-dir -r requirements.txt\n"
        f"COPY . .\nCMD [\"python\", \"-m\", \"{module}\"]\n"
    )


