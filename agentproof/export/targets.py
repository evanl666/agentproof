"""Additional export targets: OpenAI Agents SDK, CrewAI, and TypeScript.

Developers shouldn't be locked into one framework to get AgentProof's
guarantees. Each target emits a runnable repo whose *policy contract* — the
verified core — is executable and tested standalone, with framework-specific
assembly wired on top. The Python targets reuse the same generated `policy.py`
(so the safety contract is identical across frameworks); the TypeScript target
emits an equivalent `policy.ts` plus a dependency-free test.
"""

from __future__ import annotations

from pathlib import Path

from agentproof.export.langgraph import (
    _render_policy,
    _render_policy_tests,
    _render_prompts,
    _render_tools,
)
from agentproof.graph import AgentGraph, NodeType
from agentproof.scenarios import Scenario, ScenarioCategory
from agentproof.spec import BehaviorSpec, ConstraintKind


def _write(out: Path, files: dict[str, str]) -> list[Path]:
    written: list[Path] = []
    for rel, content in files.items():
        path = out / rel
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)
        written.append(path)
    return written


# ---------------------------------------------------------------------------
# OpenAI Agents SDK (Python)
# ---------------------------------------------------------------------------

def _render_openai_agent(spec: BehaviorSpec, graph: AgentGraph) -> str:
    tools = graph.nodes_of_type(NodeType.TOOL)
    limit = spec.constraint(ConstraintKind.SPEND_LIMIT)
    threshold = float(limit.params.get("threshold", 50.0)) if limit else 50.0
    tool_defs = []
    for t in tools:
        guarded = t.config.get("spend")
        body = (
            f"    decision = policy.check_refund(amount)\n"
            f"    if not decision.allowed:\n"
            f"        return f'BLOCKED by policy: {{decision.reason}}'\n"
            f"    return tools.{t.id}({{'amount': amount}})"
            if guarded
            else f"    return tools.{t.id}({{}})"
        )
        sig = "amount: float" if guarded else ""
        tool_defs.append(
            f"@function_tool\n"
            f"def {t.id}({sig}) -> str:\n"
            f'    """{t.label}."""\n'
            f"{body}\n"
        )
    tools_block = "\n\n".join(tool_defs)
    return (
        '"""OpenAI Agents SDK assembly. Generated by AgentProof.\n\n'
        "The policy gate is enforced *inside* each guarded tool, so the safety\n"
        "contract holds regardless of what the model decides to call.\n"
        '"""\n\n'
        "from agents import Agent, function_tool\n\n"
        "from agent import policy, tools\n"
        "from agent.prompts import SYSTEM_PROMPT\n\n\n"
        f"{tools_block}\n\n"
        "agent = Agent(\n"
        '    name="' + spec.name + '",\n'
        "    instructions=SYSTEM_PROMPT,\n"
        f"    model='claude-sonnet-5',\n"
        "    tools=[" + ", ".join(t.id for t in tools) + "],\n"
        ")\n"
    )


def export_openai_agents(
    spec: BehaviorSpec, graph: AgentGraph, scenarios: list[Scenario], out_dir: str | Path
) -> list[Path]:
    out = Path(out_dir)
    files = {
        "agent/__init__.py": "",
        "agent/agent.py": _render_openai_agent(spec, graph),
        "agent/tools.py": _render_tools(graph),
        "agent/policy.py": _render_policy(spec),
        "agent/prompts.py": _render_prompts(spec),
        "tests/__init__.py": "",
        "tests/test_policy.py": _render_policy_tests(spec, scenarios),
        "requirements.txt": "openai-agents>=0.1\n",
        "README.md": _readme(spec, "OpenAI Agents SDK", "python -m agent.agent"),
        ".github/workflows/ci.yml": _py_ci(),
        "Dockerfile": _py_dockerfile("agent.agent"),
    }
    return _write(out, files)


